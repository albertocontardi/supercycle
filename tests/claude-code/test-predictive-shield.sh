#!/usr/bin/env bash
# Test: predictive-shield skill
# Verifies that the shield warns about known patterns when a matching task is described

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
source "$SCRIPT_DIR/test-helpers.sh"

echo "=== Test: predictive-shield ==="
echo ""

TEST_DIR=$(create_test_project)
mkdir -p "$TEST_DIR/.claude/reports/skill_audit_notes"
mkdir -p "$TEST_DIR/.claude/skills/predictive-shield"
mkdir -p "$TEST_DIR/.claude/skills/using-supercycle"

# Create a pattern index with known CORS error on Firebase deploy
cat > "$TEST_DIR/.claude/reports/PATTERN_INDEX.md" <<'EOF'
# Pattern Index
<!-- Auto-generated by retrospective. Do not edit manually. -->
<!-- Last updated: 2026-03-15 -->

## Patterns

### cors-firebase-functions: CORS errors on Firebase Cloud Functions
- triggers: deploy, firebase, cloud-functions, functions, cors
- error: CORS header missing after function deployment
- resolution: Add CORS middleware before handler; configure allowed origins
- skill: firebase-cors
- occurrences: 5
- last_seen: 2026-03-15
- severity: HIGH

### csp-inline-handlers: CSP blocks inline onclick handlers
- triggers: deploy, hosting, csp, security, headers, onclick
- error: Content-Security-Policy blocks inline event handlers
- resolution: Refactor onclick to addEventListener; update CSP
- skill: security-check
- occurrences: 3
- last_seen: 2026-03-14
- severity: MEDIUM
EOF

# Also create quick notes backing the patterns
cat > "$TEST_DIR/.claude/reports/skill_audit_notes/2026-03-15_18-00_quick.md" <<'EOF'
# Quick Retrospective — 2026-03-15 18:00

## Tasks executed
- Deploy Firebase functions — PROBLEMATIC: CORS error

## Recurring errors
- CORS header missing on Firebase function — resolved via: manual header addition — occurrences: 2
EOF

cd "$TEST_DIR"

# Test 1: Task matching HIGH risk pattern triggers warning
echo "Test 1: Firebase deploy task triggers CORS warning..."

output=$(run_claude "I need to deploy our Firebase Cloud Functions to production. Use the predictive-shield skill to check for known risks based on the pattern index in .claude/reports/PATTERN_INDEX.md" 120 "Write,Read,Bash,Skill,Glob,Grep")

if assert_contains "$output" "CORS\|cors\|HIGH\|high risk\|Predictive Shield\|predictive" "Warns about CORS pattern"; then
    :
else
    exit 1
fi

echo ""

# Test 2: Warning includes the associated skill
echo "Test 2: Warning references the skill to activate..."

if assert_contains "$output" "firebase-cors\|pre-activat\|skill" "References the preventive skill"; then
    :
else
    echo "  [WARN] Skill reference not found — may use different phrasing"
fi

echo ""

# Test 3: Non-matching task does NOT trigger false warning
echo "Test 3: Unrelated task does not trigger warning..."

output2=$(run_claude "I want to write a new README for the project. Use the predictive-shield skill to check for known risks based on the pattern index in .claude/reports/PATTERN_INDEX.md" 120 "Write,Read,Bash,Skill,Glob,Grep")

if assert_not_contains "$output2" "HIGH.*risk\|CORS\|cors error" "No false positive for unrelated task"; then
    :
else
    echo "  [WARN] Possible false positive — broad keyword match"
fi

echo ""

# Test 4: Multiple pattern matches are prioritized
echo "Test 4: Deploy + hosting triggers both patterns, HIGH first..."

output3=$(run_claude "I need to deploy our Firebase hosting and Cloud Functions. Use the predictive-shield skill to check for known risks based on the pattern index in .claude/reports/PATTERN_INDEX.md" 120 "Write,Read,Bash,Skill,Glob,Grep")

if assert_contains "$output3" "CORS\|cors" "Identifies primary HIGH risk pattern"; then
    :
else
    exit 1
fi

if assert_contains "$output3" "CSP\|csp\|inline\|security" "Also identifies MEDIUM risk pattern"; then
    :
else
    echo "  [WARN] Secondary pattern not surfaced — may be below threshold"
fi

echo ""
echo "=== All predictive-shield tests passed ==="

cleanup_test_project "$TEST_DIR"
